def build():
    cards = []
    for num, name, up, rev in MAJORS:
        cards.append({
            "id": f"major_{num:02d}",
            "name_uk": name,
            "arcana": "major",
            "suit": None,
            "rank": num,
            "upright": up,
            "reversed": rev,
            "image": f"major_{num:02d}.png",
        })
    for slug, gen, element, meanings in SUITS:
        for (rank_slug, rank_name), (up, rev) in zip(RANKS, meanings):
            cards.append({
                "id": f"{slug}_{rank_slug}",
                "name_uk": f"{rank_name} {gen}",
                "arcana": "minor",
                "suit": slug,
                "element": element,
                "rank": rank_slug,
                "upright": up,
                "reversed": rev,
                "image": f"{slug}_{rank_slug}.png",
            })

    assert len(cards) == 78, f"expected 78 cards, got {len(cards)}"
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(json.dumps(cards, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"wrote {len(cards)} cards -> {OUT}")


if __name__ == "__main__":
    build()
